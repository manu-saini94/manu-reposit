package com.bridgelabz.clinique.service;

public class Patient {
	private String pat_name;
	private String pat_id;
	private String pat_mob;
	private String pat_age;
	
	public String getPat_name() {
		return pat_name;
	}
	public void setPat_name(String pat_name) {
		this.pat_name = pat_name;
	}
	public String getPat_id() {
		return pat_id;
	}
	public void setPat_id(String pat_id) {
		this.pat_id = pat_id;
	}
	public String getPat_mob() {
		return pat_mob;
	}
	public void setPat_mob(String pat_mob) {
		this.pat_mob = pat_mob;
	}
	public String getPat_age() {
		return pat_age;
	}
	public void setPat_age(String pat_age) {
		this.pat_age = pat_age;
	}

}
