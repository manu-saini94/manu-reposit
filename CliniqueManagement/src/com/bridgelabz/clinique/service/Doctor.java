package com.bridgelabz.clinique.service;

public class Doctor {
private String doc_name;
private String doc_id;
private String doc_spec;
private String doc_avail;

public String getDoc_name() {
	return doc_name;
}
public void setDoc_name(String doc_name) {
	this.doc_name = doc_name;
}
public String getDoc_id() {
	return doc_id;
}
public void setDoc_id(String doc_id) {
	this.doc_id = doc_id;
}
public String getDoc_spec() {
	return doc_spec;
}
public void setDoc_spec(String doc_spec) {
	this.doc_spec = doc_spec;
}
public String getDoc_avail() {
	return doc_avail;
}
public void setDoc_avail(String doc_avail) {
	this.doc_avail = doc_avail;
}


}
